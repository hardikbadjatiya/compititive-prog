
import java.util.Scanner;

public class frenchGame {
    
public static void main(String[] args) {
   Scanner sc=new Scanner (System.in);
   int n=sc.nextInt();
   for(int k=0;k<n;k++)
   {
       int ar[][]=new int[9][9];
   for(int i=0;i<9;i++)
   {
       for(int j=0;j<9;j++)
       {
           ar[i][j]=sc.nextInt();
       }
   } 
 
   }
}
    
}
